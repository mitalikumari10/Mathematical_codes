#include<iostream>
using namespace std;
int main()
{
    int num,i,even,odd,sum=0;
    cout<<"enter the max number:"<<endl;
    cin>>num;
    for(i=1;i<=num;i++);
     {
      if (i%2==0)
      {
        cout<<even;
      }
      else
      {
        cout<<odd;
      }
      sum=sum+odd;
     }
      cout<<sum;
      
    
}